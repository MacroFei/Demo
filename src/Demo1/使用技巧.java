package Demo1;

public class 使用技巧 {
	public static void main(String[] args) {
		System.out.println();
		for (int i = 0; i < args.length; i++) {
			/**
			 * 批量修改 ALT+SHIFT+R
			 * 
			 * 快速输入主方法 1. 敲入main 2. alt+/
			 * 
			 * 快速输入System.out.println 顶 折 纠 问 1. 敲入syso 2. alt+/
			 * 
			 * 快速输入for 顶 折 纠 问 1. 敲入for 2. alt+/
			 * 
			 * 查看java源代码 顶 折 纠 问 JDK里提供的类，比如String,Integer,System都是开源的，免费提供其源代码
			 * 可以很简便的通过eclipse就查看源码 1. 先选中想要查看的方法，比如System.out.println()的 println 2. 敲键F3
			 * 
			 * 代码格式化 顶 折 纠 问 ctrl+shift+f 格式化代码 注意： 这个快捷键和搜狗输入法的切换简繁冲突。 所以最好把搜狗的切换简繁取消掉。
			 *
			 * 快速助手 顶 折 纠 问 ctrl+shift+o import 类 去掉未使用的类 等等都可以使用
			 * 
			 * 快速注释 顶 折 纠 问 选中一行或者多行代码 ctrl+shift+c
			 * 
			 * 大小写转换 顶 折 纠 问 ctrl+shift+x 选中的转换为大写 ctrl+shift+y 选中的转换为小写
			 *
			 * 快速解决办法提示 顶 折 纠 问 在出错误的行，使用ctrl+1。 比如忘记导入类了，就可以用这个解决
			 *
			 * toString getter setter 构造方法的快速生成 顶 折 纠 问 1. 通过菜单-> Source 就可以看到快速生成的选项
			 * 
			 * 2. 直接使用快捷键 shift+alt+s也可以弹出这个选项
			 */
		}
	}

}
